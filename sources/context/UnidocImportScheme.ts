  /**
   * 
   */
  export function get(name: string): string {
    let left = 0
    let right = ALL.length

    while (left !== right) {
      const cursor = left + ((right - left) >>> 1)
      const comparison = name.localeCompare(ALL[cursor])

      if (comparison === 0) {
        return ALL[cursor]
      } else if (comparison > 0) {
        left = cursor + 1
      } else {
        right = cursor
      }
    }

    return name
  }
